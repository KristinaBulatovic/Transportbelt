                glina , pesak, šljunak, so, zemlja, glina i drugo. Ove trake su standardno otporne na temperaturu
                okoline od - 25°C do +60°C. Temperatura transportovanog materijala do +60°C. Pogodne su za transport
                materijala pod uglom od 20° </p>
            <br>
            <h3 class="text-center">Oznake gumene obloge</h3>
            <br>
            <p class="text-justify">
                ● X(antiabrazivna)  – Namenjene su za transport abrazivnih materijala i oštrih materijala velikih dimenzija(kamen, staklo itd.);<br>
                ● W(super antiabrazivna) – Namenjene su za transport vrlo abrazivnog materijala(oštrougaonog sitnog materijala)<br>
                ● Y(normal) – Gumene transportne trake namenjene za transport sitnog abrazivnog materijala(pesak, šljunak)<br>
                ● R – Gumene transportne trake otporne na hladnoću i namenjene su za eksplotaciju pri temperaturnom režimu od -60°C do +60°C<br>
                ● T(Toplootporne) za transport toplih i vrućih materijala(do 200°C)<br><br>
                Gumene transportne trake proizvode se u sledećim standardnim širinama:
                200, 300, 400, 500, 600, 650, 800, 1000, 1200, 1300, 1400, 1600, 2000, 2200, 2400, 2500, 2800, 3000  mm.<br><br>

