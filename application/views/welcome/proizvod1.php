            <h2 class="text-center">Gumene transportne trake opšte namene <br> "Y" (N), "X", "W", "Z" </h2>
            <br>
            <p class="text-justify">Gumene transportne trake opšte namene "Y" (N), "X", "W", "Z" namenjene su za prenos komadnih i
